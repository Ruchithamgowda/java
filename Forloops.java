class Forloops {
    public static void main(String[] args) {
        String[] shirts = {"T-Shirt", "Casual Shirts", "Polo Shirts", "Hoodie", "Formal Shirt"};
        for (int i = 0; i < shirts.length; i++) {
            System.out.println("Shirts: " + shirts[i]);
        }
        System.out.println("------------------------");
    }
}
